                                            <th class="px-4 py-3 text-right text-[11px] font-bold tracking-[0.16em] text-slate-500 uppercase">
                                                Aksi
                                            </th>
                                        </tr>
                                    </thead>
                                    <tbody>
                                        <tr
                                            v-for="(component, index) in form.components"
                                            :key="`desktop-${index}-${component.sort_order}`"
                                            class="border-b border-zinc-200 align-top last:border-b-0"
                                        >
                                            <td class="px-4 py-3 text-sm font-bold text-zinc-700">
                                                {{ index + 1 }}
                                            </td>
                                            <td class="px-4 py-2">
                                                <Input
                                                    v-model="component.name"
                                                    type="text"
                                                    placeholder="Contoh: Disiplin"
                                                    class="h-10 w-full rounded-lg border-zinc-200 bg-zinc-50"
                                                />
                                                <InputError
                                                    class="mt-1"
                                                    :message="componentError(index, 'name')"
                                                />
                                            </td>
                                            <td class="px-4 py-2">
                                                <Input
                                                    v-model="component.weight_percentage"
                                                    type="number"
                                                    min="0"
                                                    step="0.01"
                                                    class="h-10 w-full rounded-lg border-zinc-200 bg-zinc-50"
                                                />
                                                <InputError
                                                    class="mt-1"
                                                    :message="componentError(index, 'weight_percentage')"
                                                />
                                            </td>
                                            <td class="px-4 py-2">
                                                <div class="flex items-center justify-end gap-2">
                                                    <Button
                                                        type="button"
                                                        variant="outline"
                                                        size="sm"
                                                        class="h-9 w-9 rounded-lg border-zinc-200 p-0 text-zinc-700"
                                                        :disabled="index === 0"
                                                        @click="moveComponent(index, 'up')"
                                                    >
                                                        <ArrowUp class="size-4" />
                                                    </Button>
                                                    <Button
                                                        type="button"
                                                        variant="outline"
                                                        size="sm"
                                                        class="h-9 w-9 rounded-lg border-zinc-200 p-0 text-zinc-700"
                                                        :disabled="index === form.components.length - 1"
                                                        @click="moveComponent(index, 'down')"
                                                    >
                                                        <ArrowDown class="size-4" />
                                                    </Button>
